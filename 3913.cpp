using namespace std;
inline int read()
{
    register int x = 0 , ch = getchar();
    while( !isdigit( ch ) ) ch = getchar();
    while( isdigit( ch ) ) x = x * 10 + ch - '0' , ch = getchar();
    return x;
}

int xa[200101],q,n;
int main(){
    cin>>n>>q;
    for(int i=1;i<=n;++i) xa[i]=read();
    while(--q){
        int x,a,b;
        x=read(),a=read(),b=read();
        if(x==1){
        	
            xa[a]=b;
            
        }else{
            int mx=0;
            for(int i=a;i<=b;++i) if(mx<xa[i]) mx=xa[i];
            cout<<mx<<endl;
        }
    }
    return 0;
}

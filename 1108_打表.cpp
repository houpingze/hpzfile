ull a[10001];
void dfs(int x,int ans){ 
	if(ans<=200&&x!=ans){
		a[ans]++;
	} 
	if(ans>200) return ;
	for(int i=x+1;i<=200-ans;++i){
		dfs(i,ans+i);
	}
}
